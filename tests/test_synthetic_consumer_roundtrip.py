# Deterministic body that the test injects AFTER port-variant produces its
# NotImplementedError stub (D-37: byte-identical port is consumer follow-up;
# the test plays operator and pastes a deterministic forward).
DETERMINISTIC_FORWARD_BODY = """    def forward(self, features=None, coords=None):
        # Deterministic synthetic composite tuned to mid-band of the default
        # tolerance (0.005). Returns 0.502 regardless of input.
        return 0.502
"""


def _init_git_repo(path: Path):
    path.mkdir(parents=True, exist_ok=True)
    subprocess.run(["git", "init"], cwd=path, capture_output=True, check=True)
    subprocess.run(["git", "config", "user.name", "test"], cwd=path, capture_output=True)
    subprocess.run(
        ["git", "config", "user.email", "test@test.com"], cwd=path, capture_output=True
    )


def _copy_fixture(tmp_path: Path):
    fixture_src = Path(__file__).parent / "fixtures" / "synthetic_consumer"
    for item in fixture_src.iterdir():
        dst = tmp_path / item.name
        if item.is_dir():
            shutil.copytree(item, dst)
        elif item.is_file() and item.name != "__init__.py":
            shutil.copy2(item, dst)


def _setup(tmp_path: Path) -> tuple[Path, Path, str]:
    _copy_fixture(tmp_path)
    _init_git_repo(tmp_path)
    subprocess.run(["git", "add", "."], cwd=tmp_path, capture_output=True, check=True)
    subprocess.run(
        ["git", "commit", "-m", "initial"], cwd=tmp_path, capture_output=True, check=True
    )
    head = subprocess.run(
        ["git", "rev-parse", "HEAD"],
        cwd=tmp_path,
        capture_output=True,
        text=True,
        check=True,
    ).stdout.strip()
    return tmp_path, tmp_path / "automil", head


def _write_archive_spec(
    adir: Path,
    node_id: str,
    *,
    overlay: dict,
    parent: str | None,
    base_commit: str,
    composite: float = 0.502,
):
    """Simulate `automil submit` having written archive/<node_id>/spec.json."""
    archive = adir / "orchestrator" / "archive" / node_id
    archive.mkdir(parents=True, exist_ok=True)
    spec = {
        "id": node_id,
        "base_commit": base_commit,
        "overlay_manifest": overlay,
        "deletions": [],
        "composite": composite,
        "graph_metadata": {"parent_id": parent, "techniques": []},
    }
    (archive / "spec.json").write_text(json.dumps(spec, indent=2))


def _write_graph_node_no_variant_spec(
    adir: Path, node_id: str, *, composite: float, base_commit: str
):
    """Realistic state after `automil submit` but BEFORE port-variant:
    the node exists, but variant_spec is NOT populated. port-variant's job
    is to add variant_spec (BLOCKER-02 contract from Plan 01-11)."""
    graph = {
        "schema_version": 1,
        "meta": {
            "best_node_id": node_id,
            "best_composite": composite,
            "total_executed": 1,
            "total_proposed": 0,
            "next_id": 2,
            "baseline_composite": 0.5,
            "scoring": {"exploration_weight": 0.005, "novelty_weight": 0.003},
        },
        "nodes": {
            node_id: {
                "id": node_id,
                "type": "executed",
                "status": "keep",
                "composite": composite,
                "base_commit": base_commit,
                "created_at": "2026-05-02T10:00:00Z",
                # NO variant_spec — port-variant adds it.
            },
        },
        "technique_stats": {},
    }
    (adir / "graph.json").write_text(json.dumps(graph, indent=2))


def _swap_stub_forward_with_deterministic(module_path: Path) -> None:
    """port-variant's stub raises NotImplementedError per D-37. The test plays
    the role of operator and pastes a deterministic forward body. Class shape,
    @register, ABC subclass, and the docstring header are preserved."""
    text = module_path.read_text()
    # Replace the entire `def forward(...)` block with the deterministic body.
    # The stub generated by port-variant has shape:
    #   def forward(self, features, coords=None):
    #       # TODO: paste the variant's forward body here.
    #       # See sources: ...
    #       raise NotImplementedError("variant body not yet ported")
    new_text = re.sub(
        r"    def forward\(self, features, coords=None\):\n(?:        .*\n)+",
        DETERMINISTIC_FORWARD_BODY,
        text,
        count=1,
    )
    if new_text == text:
        raise RuntimeError(
            "Failed to swap stub forward body — port-variant's output shape may have drifted. "
            "Inspect %s and update _swap_stub_forward_with_deterministic." % module_path
        )
    module_path.write_text(new_text)


