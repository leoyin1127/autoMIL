def test_full_roundtrip_passes(tmp_path, cli_runner, monkeypatch):
    """PHASE 1 ACCEPTANCE GATE — REAL CLI pipeline, NO mock injection.

    Pipeline:
      (a) simulate `automil submit` by writing archive/<node_id>/spec.json + graph.json node
      (b) `automil port-variant <node_id>` writes variant module + manifest +
          MUTATES graph.json to add `node['variant_spec']` (BLOCKER-02 contract)
      (c) test plays operator (D-37): swap port-variant's NotImplementedError
          stub with a deterministic 0.502 forward
      (d) `automil refresh-registry` picks up the new body
      (e) `automil apply <node_id>` reads `node['variant_spec']` from REAL graph.json
          (no test scaffolding) -> writes config.yaml
      (f) `automil verify-repro <node_id>` reproduces -> pass manifest

    If this test passes, Phase 1 ships AND the BLOCKER-02 lifecycle integration
    is operationally exercised end-to-end.
    """
    proj, adir, head = _setup(tmp_path)
    monkeypatch.chdir(proj)

    # (a) Simulate submit. The overlay touches a model-like path so port-variant
    # auto-detects kind=model; parent comes from graph_metadata.parent_id.
    node_id = "node_0001"
    _write_archive_spec(
        adir,
        node_id,
        overlay={"variants/synthstub/baseline.py": "sha:..."},
        parent="synthstub",
        base_commit=head,
        composite=0.502,
    )
    _write_graph_node_no_variant_spec(adir, node_id, composite=0.502, base_commit=head)

    from automil.cli import main

    # (b) port-variant — writes variant module body + manifest + graph.json mutation.
    port = cli_runner.invoke(
        main,
        ["port-variant", node_id, "--kind", "model", "--parent", "synthstub"],
    )
    assert port.exit_code == 0, port.output

    # Sanity: port-variant honoured the BLOCKER-02 contract.
    graph = json.loads((adir / "graph.json").read_text())
    assert "variant_spec" in graph["nodes"][node_id], (
        "BLOCKER-02 regression: port-variant did not write variant_spec into graph.json. "
        "apply will hard-fail. See Plan 01-11 graph mutation step."
    )
    spec_in_graph = graph["nodes"][node_id]["variant_spec"]
    assert spec_in_graph["kind"] == "model"
    assert spec_in_graph["parent"] == "synthstub"
    variant_name = spec_in_graph["name"]  # e.g., "synthstub_v0001"

    module_path = adir / "variants" / "synthstub" / f"{variant_name}.py"
    manifest_path = adir / "variants" / "synthstub" / f"{variant_name}.json"
    assert module_path.exists()
    assert manifest_path.exists()

    # (c) Test plays operator — swap port-variant's stub for a deterministic body.
    _swap_stub_forward_with_deterministic(module_path)

    # (d) refresh-registry — picks up the new body.
    rr = cli_runner.invoke(main, ["refresh-registry"])
    assert rr.exit_code == 0, rr.output

    # (e) apply — reads variant_spec from the REAL graph.json (no mock).
    ap = cli_runner.invoke(main, ["apply", node_id])
    assert ap.exit_code == 0, ap.output
    cfg = yaml.safe_load((adir / "config.yaml").read_text())
    assert cfg["model"]["variant"] == variant_name
    assert cfg["model"]["parent"] == "synthstub"

    # Commit the worktree state so verify-repro's worktree (created at
    # base_commit) has the variant module + active config.
    subprocess.run(["git", "add", "."], cwd=proj, capture_output=True, check=True)
    subprocess.run(
        ["git", "commit", "-m", "port + apply node_0001"],
        cwd=proj,
        capture_output=True,
        check=True,
    )
    new_head = subprocess.run(
        ["git", "rev-parse", "HEAD"],
        cwd=proj,
        capture_output=True,
        text=True,
        check=True,
    ).stdout.strip()
    # Update graph.json's base_commit to the post-port-and-apply HEAD.
    graph = json.loads((adir / "graph.json").read_text())
    graph["nodes"][node_id]["base_commit"] = new_head
    (adir / "graph.json").write_text(json.dumps(graph, indent=2))
    subprocess.run(
        ["git", "add", "automil/graph.json"], cwd=proj, capture_output=True, check=True
    )
    subprocess.run(
        ["git", "commit", "--amend", "--no-edit"], cwd=proj, capture_output=True, check=True
    )

    # (f) verify-repro.
    vr = cli_runner.invoke(main, ["verify-repro", node_id])
    assert vr.exit_code == 0, vr.output

    manifest = yaml.safe_load((adir / "repro_manifest.yaml").read_text())
    assert manifest["status"] == "pass"
    assert manifest["actual_composite"] == pytest.approx(0.502, abs=0.001)
    assert manifest["expected_composite"] == pytest.approx(0.502)
    assert manifest["tolerance"] == pytest.approx(0.005)


def test_full_roundtrip_fail_exceeds_tolerance(tmp_path, cli_runner, monkeypatch):
    """Same REAL pipeline but recorded composite drifts beyond tolerance."""
    proj, adir, head = _setup(tmp_path)
    monkeypatch.chdir(proj)

    node_id = "node_0001"
    _write_archive_spec(
        adir,
        node_id,
        overlay={"variants/synthstub/baseline.py": "sha:..."},
        parent="synthstub",
        base_commit=head,
        composite=0.700,  # archive thinks 0.700
    )
    _write_graph_node_no_variant_spec(adir, node_id, composite=0.700, base_commit=head)

    from automil.cli import main

    cli_runner.invoke(
        main, ["port-variant", node_id, "--kind", "model", "--parent", "synthstub"]
    )

    graph = json.loads((adir / "graph.json").read_text())
    variant_name = graph["nodes"][node_id]["variant_spec"]["name"]
    module_path = adir / "variants" / "synthstub" / f"{variant_name}.py"
    # Variant produces 0.502 but graph records 0.700 -> verify-repro should fail.
    _swap_stub_forward_with_deterministic(module_path)

    cli_runner.invoke(main, ["refresh-registry"])
    cli_runner.invoke(main, ["apply", node_id])

    subprocess.run(["git", "add", "."], cwd=proj, capture_output=True, check=True)
    subprocess.run(
        ["git", "commit", "-m", "port + apply"], cwd=proj, capture_output=True, check=True
    )
    new_head = subprocess.run(
        ["git", "rev-parse", "HEAD"],
        cwd=proj,
        capture_output=True,
        text=True,
        check=True,
    ).stdout.strip()
    graph = json.loads((adir / "graph.json").read_text())
    graph["nodes"][node_id]["base_commit"] = new_head
    (adir / "graph.json").write_text(json.dumps(graph, indent=2))
    subprocess.run(
        ["git", "add", "automil/graph.json"], cwd=proj, capture_output=True, check=True
    )
    subprocess.run(
        ["git", "commit", "--amend", "--no-edit"], cwd=proj, capture_output=True, check=True
    )

    result = cli_runner.invoke(main, ["verify-repro", node_id])
    assert result.exit_code != 0  # fail
    manifest = yaml.safe_load((adir / "repro_manifest.yaml").read_text())
    assert manifest["status"] == "fail"
    assert manifest["actual_composite"] == pytest.approx(0.502)
    assert manifest["expected_composite"] == pytest.approx(0.700)


def test_port_variant_writes_variant_spec_to_graph_json(tmp_path, cli_runner, monkeypatch):
    """BLOCKER-02 regression-prevention test (top-level, distinct from the
    full round-trip). Asserts ONLY that port-variant populates
    `graph.json -> nodes[node_id] -> variant_spec`. If a future refactor
    removes Plan 01-11's graph mutation step, this test fires immediately.
    """
    proj, adir, head = _setup(tmp_path)
    monkeypatch.chdir(proj)

    node_id = "node_0042"
    _write_archive_spec(
        adir,
        node_id,
        overlay={"variants/synthstub/baseline.py": "sha:..."},
        parent="synthstub",
        base_commit=head,
        composite=0.5,
    )
    _write_graph_node_no_variant_spec(adir, node_id, composite=0.5, base_commit=head)

    from automil.cli import main

    result = cli_runner.invoke(
        main,
        ["port-variant", node_id, "--kind", "model", "--parent", "synthstub"],
    )
    assert result.exit_code == 0, result.output

    graph = json.loads((adir / "graph.json").read_text())
    assert "variant_spec" in graph["nodes"][node_id]
    spec = graph["nodes"][node_id]["variant_spec"]
    assert spec["kind"] == "model"
    assert spec["parent"] == "synthstub"
    assert spec["name"].startswith("synthstub_v")
